    private void btnruajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnruajActionPerformed
        // TODO add your handling code here:
        if (txtiddhoma.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "jepni seleksionimin e dhomes");
            txtiddhoma.requestFocus();
            return;
        }
        if (txtidklienti.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "jepni seleksionimin e klientit");
            txtidklienti.requestFocus();
            return;
        }

        if (txtkostoja_e_strehimit.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "jepni koston e strehimit");
            txtkostoja_e_strehimit.requestFocus();
            return;
        }

        vrezervimi dts = new vrezervimi();
        frezervimi func = new frezervimi();

        dts.setIddhoma(Integer.parseInt(txtiddhoma.getText()));
        dts.setIdklienti(Integer.parseInt(txtidklienti.getText()));
        dts.setIdpunonjes(iduser);

        int seleccionado = cbotipi_rezervimit.getSelectedIndex();
        dts.setTipi_rezervimit((String) cbotipi_rezervimit.getItemAt(seleccionado));

        Calendar cal;
        int d,m,a;
        cal=dcdata_rezervimit.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        dts.setData_rezervimit(new Date(a,m,d));

        cal=dcdat_hyrje.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        dts.setDat_hyrje(new Date(a,m,d));

        cal=dcdat_dalje.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        dts.setDat_dalje(new Date(a,m,d));

        dts.setKostoja_e_strehimit(Double.parseDouble(txtkostoja_e_strehimit.getText()));
        seleccionado = cbodhoma_e_zene.getSelectedIndex();
        dts.setDhoma_e_zene((String) cbodhoma_e_zene.getItemAt(seleccionado));

        if (accion.equals("ruaj")) {
            if (func.shto(dts)) {
                JOptionPane.showMessageDialog(rootPane, "rezervimi u rregjistrua me sukses");
                moster("");
                inavantazh();

                //ocupamos la Habitaci√≥n alquilada
                fdhoma func3= new fdhoma();
                vdhoma dts3 = new vdhoma();

                dts3.setIddhoma(Integer.parseInt(txtiddhoma.getText()));
                func3.zene(dts3);
            }

        }
        else if (accion.equals("shkruaj")){
            dts.setIdrezervimi(Integer.parseInt(txtidrezervimi.getText()));
            dts.setIdpunonjes(Integer.parseInt(txtidpunonjes.getText()));
            if (func.shkruaj(dts)) {
                JOptionPane.showMessageDialog(rootPane, "rezervimi u shkrua me sukses");
                moster("");
                inavantazh();
            }
        }
    }//GEN-LAST:event_btnruajActionPerformed

